#fortune cookie program

import random

fortune = random.randit(0.4)

if fortune == 0:
    print("May you one day be carbon neutral")
elif fortune == 1:
    print("You have rice in your teeth")
elif fortune == 2:
    print("May the money in your life work for you")
elif fortune == 3:
    print("You will find love at your next turn")
elif fortune == 4:
    print("Fortune isn't here, maybe in your next cookie.")